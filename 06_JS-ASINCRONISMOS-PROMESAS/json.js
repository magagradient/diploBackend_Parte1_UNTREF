// es un formato de archivo que contiene informacion 

// JSON es el acronimo de javaScript Object Notation, y como su nombre lo indica, es muy similar al objeto literal que ya conocemos.

// es un formato de datos ligero y muy utilizado para el intercambio de informacion entre un servidor y un cliente, o entre diferentes partes de una aplicacion. 
// es facilmente legible tanto para los humanos como para las maquinas, lo que lo hace muy popular en el mundo de la programacion y la web. 

// legibilidad: los datos en este formato son legibles para los humanos, ya que utiliza una estructura jerarquica que se asemeja a los diccionarios y listas. 

// Independiente del lenguaje: Json es independiente del lenguaje de programacion, lo que significa que se puede utilizar en una amplia variedad de lenguajes de programacion.

// LIGERO: es un formato de datos ligeros, lo que significa que no agrega una gran cantidad de sobrecarga de datos al contenido. Esto es importante cuando se transfiere informacion a traves de redes, como en aplicaciones web o servicios web. 

// AMPLIA ADOPCION: es ampliamente adopado en la industtria de desarrollo de software y es el formato de eleccion para muchas API web, bases de datos NoSQL y configuraciones de datos en muchas aplicaciones.

// ///////////////////////////////////////////////////

// .parse() = covierte a un json en un objeto de javasript
// se utiliza para convertir una cadena de JSON en un objeto javaScript. Toma una cadena JSON como argumento y devuelve un objeto javaScript que representa los datos contenidos en esa cadena JSON. Esto permite que los datos sean utilizados y manipulados en el codigo javaScript. 

// JSON.parse() se utiliza comunmente cuando se reciben datos desde una API web o un servicio web en formato JSON. Permite convertir esos datos en objetos javaScript que se pueden utilizar en la logica de la aplicacion. 

// .stringify() = cobierte a un objeto de javascript en json.

// JSON.stringify() es util cuando deseas enviar datos estructurados a un servidor, como enviar un formulario con datos de usuario. Tambien se utiliza para almacenar datos complejos en el almacenamiento local o enviar datos a traves de una conexion de red. 


